class FoodItemList {
  List<FoodItem> foodItems;
  FoodItemList({@required this.foodItems});
}

class FoodItem {
  int id;
  String title;
  double price;
  String imgUrl;
  String type;
  String time;
  String rate;
  String fees;
  int quantity;

  FoodItem({
    @required this.id,
    @required this.title,
    @required this.type,
    @required this.price,
    @required this.imgUrl,
    @required this.time,
    @required this.rate,
    @required this.fees,
    this.quantity = 1
  });

  void incrementQuantity( ) {
    this.quantity = this.quantity + 1;
  }

  void decrementQuantity( ) {
    this.quantity = this.quantity - 1;
  }
}